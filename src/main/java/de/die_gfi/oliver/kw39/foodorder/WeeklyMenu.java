public class WeeklyMenu {

    ArrayList<String> mon;
    ArrayList<String> tue;
    ArrayList<String> wed;
    ArrayList<String> thu;
    ArrayList<String> fri;

    public WeeklyMenu(ArrayList<String> mon, ArrayList<String> tue, ArrayList<String> wed, ArrayList<String> thu, ArrayList<String> fri) {
        this.mon = mon;
        this.tue = tue;
        this.wed = wed;
        this.thu = thu;
        this.fri = fri;
    }



}
